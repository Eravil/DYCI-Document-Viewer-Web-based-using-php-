                    <article>
                        ADMISSION REQUIREMENTS
                        A student is admitted with definite understanding that he and his parents or guardians agree to comply with the scholastic standards of the College and its rules governing student behavior. The Administration is empowered to deal with violations in accordance with its system of discipline and guidance.

                        A student seeking admission to the College must be a graduate of a standard secondary level offered by a duly recognized school and must have taken the DYCI admission test. A student whose eligibility for admission has been verified must submit to the Registrar or his authorized representatives the following:
                        <br><br>
                        <b>Incoming Freshman</b><br><br>
                        High School Report Card (Form 138)<br>
                        Three (3) copies size 2 x 2 photos in white background<br>
                        Certificate of Good Moral Character<br>
                        Medical Examination Certificate Issued by Dr. Yanga’s Hospital<br>
                        Birth Certificate Issued by the PSA<br><br>
                        <b>Student Transferees</b><br><br>
                        <b>A student from another school seeking transfer to any College in DYCI</b> must submit the following credentials:<br><br>
                        Honorable Dismissal<br>
                        Certificate of Good Moral character<br>
                        An official copy of grades or transcript of records duly signed by the Dean or Registrar<br>
                        A letter of application indicating his reasons for seeking transfer to DYCI as well as the degree program he/she wishes to pursue<br>
                        Medical Examination Certificate issued by Dr. Yanga’s Hospital<br>
                        Birth certificate issued by PSA<br>
                        Three (3) pieces sized 2 x 2 photo in white background<br><br>
                        <b>A DYCIAN student seeking transfer to another College in DYCI </b>must submit the following credentials to the Office of the Dean where she/he is intending to transfer:<br>
                        A letter of application indicating her/his reason for seeking transfer to the College as well as the degree programs that he/she wishes to pursue<br>
                        Complete certified true copy of grades<br>
                        Permit to transfer from the previous College where the student belongs
                        Clearance<br>
                        Three copies of 2 x 2 photo in white background<br>
                        Applicants may be asked to submit additional requirements as required by the specific program/college unit’s CMOs (Commission on Higher Education Memorandum Orders).<br>
